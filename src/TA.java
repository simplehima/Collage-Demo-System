    private void submit_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submit_btnActionPerformed
     //===== Start OF GUI CODE For Input =====
     ta TA1 = new ta(); // TA Object 
      
     int ID = Integer.parseInt(id_TF.getText());
     TA1.setid(ID);
     
     String name = name_TF.getText();
     TA1.setname(name);
     
    
     
     if (RB_advisor.isSelected()){
         
       a1.setsalary(5000);
       jL_salary.setText(Double.toString(a1.getsalary()));
        jL_kind.setText("Advisor");
   
     }else if (RB_ta.isSelected()){
         
       TA1.setsalary(5000);
       jL_salary.setText(Double.toString(TA1.getsalary()));    
       jL_kind.setText("Normal TA");
    
     }
     
     
     
     
     //OUTPUT
     jL_name.setText(name);
     jL_id.setText(Integer.toString(ID));
    
     
     
     
    }//GEN-LAST:event_submit_btnActionPerformed
  
