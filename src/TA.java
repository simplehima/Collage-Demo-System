class ta {
     public String name;
    public  int id;
    public double salary;



    public void setname (String name) {
        this.name=name;
    }
    public String getname (){
        return name;
    }
    public void setid (int id){
        this.id=id;
    }
    public int getid(){
        return id;
    }
    public void setsalary(double salary){
        this.salary=salary;
    }
    public double getsalary(){
        return salary;
    }
}

class Advisor extends ta{
    public int cours = 0;

    public void addCours(int addcours){
       this.cours = cours + addcours; 
    }
    public int getaddCours(){
       return cours; 
    }
    public void dropCours(int dropcours){
      if (cours >= dropcours){
        this.cours = cours - dropcours;
      }
    }
    public int getdropCours(){
       return cours; 
    }

    @Override
     public void setsalary(double salary){
        this.salary = salary  + 2000;
    }
     @Override
    public double getsalary(){
        return salary;
    }
}
